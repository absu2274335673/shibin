package net.engyne.po;

import java.util.ArrayList;
import java.util.List;

public class RealTemplate {
	
	private String result;
	private List<Template> list=new ArrayList<Template>();
	private int total;
	public String getResult() {
		return result;
	}
	public void setResult(String result) {
		this.result = result;
	}
	public List<Template> getList() {
		return list;
	}
	public void setList(List<Template> list) {
		this.list = list;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	@Override
	public String toString() {
		return "RealTemplate [result=" + result + ", list=" + list + ", total="
				+ total + "]";
	}
	
}
