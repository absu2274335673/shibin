package net.engyne.domain;

import java.util.ArrayList;
import java.util.List;

public class Msg
{
	private Content content;
	private Extra extra;
	private String convid;
	private String from;
	private List<String>  read=new ArrayList<String>();
	public Content getContent() {
		return content;
	}
	public void setContent(Content content) {
		this.content = content;
	}
	public Extra getExtra() {
		return extra;
	}
	public void setExtra(Extra extra) {
		this.extra = extra;
	}
	public String getConvid() {
		return convid;
	}
	public void setConvid(String convid) {
		this.convid = convid;
	}
	public String getFrom() {
		return from;
	}
	public void setFrom(String from) {
		this.from = from;
	}
	public List<String> getRead() {
		return read;
	}
	public void setRead(List<String> read) {
		this.read = read;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getTmpindex() {
		return tmpindex;
	}
	public void setTmpindex(String tmpindex) {
		this.tmpindex = tmpindex;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	private String time;
	private String tmpindex;
	private String type;
}
